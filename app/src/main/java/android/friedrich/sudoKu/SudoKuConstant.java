package android.friedrich.sudoKu;

public interface SudoKuConstant {
    /**
     * the number of a uncertain cell
     */
    byte NUMBER_UNCERTAIN = 0;

    /**
     * numbers from one to nine
     */
    byte NUMBER_ONE = 1;
    byte NUMBER_TWO = 2;
    byte NUMBER_THREE = 3;
    byte NUMBER_FOUR = 4;
    byte NUMBER_FIVE = 5;
    byte NUMBER_SIX = 6;
    byte NUMBER_SEVEN = 7;
    byte NUMBER_EIGHT = 8;
    byte NUMBER_NINE = 9;

    /**
     * the number of cells in a SudoKu board
     */
    int BOARD_CELL_SIZE = 81;

    /**
     * the number of rows in a SudoKu board
     */
    int BOARD_ROW_SIZE = 9;

    /**
     * the number of cols in a SudoKu board
     */
    int BOARD_COL_SIZE = 9;

    /**
     * the number of cells in a SudoKu unit
     */
    int UNIT_CELL_SIZE = 9;
}
